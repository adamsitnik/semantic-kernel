using System;

namespace Microsoft.Extensions.VectorData;

/// <summary>
/// Attribute to mark a property on a record class as the key under which the record is stored in a vector store.
/// </summary>
/// <remarks>
/// The characteristics defined here will influence how the property is treated by the vector store.
/// </remarks>
[AttributeUsage(AttributeTargets.Property, AllowMultiple = false)]
public sealed class VectorStoreRecordKeyAttribute : Attribute
{
    /// <summary>
    /// Gets or sets an optional name to use for the property in storage, if different from the property name.
    /// E.g. the property name might be "MyProperty" but the storage name might be "my_property".
    /// </summary>
    public string? StoragePropertyName { get; set; }

    /// <summary>
    /// Gets or sets whether the key should be auto-generated by the vector store.
    /// </summary>
    /// <value>
    /// The default is <see langword="false" />.
    /// </value>
    /// <remarks>
    /// If set to <see langword="false" />, you must set the key property on any record that you pass to <see cref="IVectorStoreRecordCollection{TKey, TRecord}.UpsertAsync(TRecord, UpsertRecordOptions?, System.Threading.CancellationToken)"/>.
    /// If set to <see langword="true" />, the key property may be left null on any record that you pass to <see cref="IVectorStoreRecordCollection{TKey, TRecord}.UpsertAsync(TRecord, UpsertRecordOptions?, System.Threading.CancellationToken)"/>
    /// and a generated key will be returned.
    /// </remarks>
    public bool AutoGenerate { get; set; }
}
